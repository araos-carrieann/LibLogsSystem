    public void countDown() {
        timer = new Timer();
        TimerTask timerTask = new TimerTask() {
            private int remainingTime = waitTime;

            @Override
            public void run() {
                if (remainingTime > 0) {
                    remainingTime--;
                } else {
                    timer.cancel();
                    timer.purge();
                    dispose(); // Close the dialog
                }
            }
        };

        // Schedule the timer task to run every second
        timer.scheduleAtFixedRate(timerTask, 0, 1000);
    }
